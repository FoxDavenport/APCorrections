public class Unit6_Q5
{
    public static void main(String[]args){
        int[] arr = {4, 3, 2, 1, 0};
int total = 0;
for (int k = 0; k <= total; k++)
{
if (arr[k] % 2 == 0)
{
total += arr[k];
}
else
{
total -= arr[k];
}
}
System.out.print(total);
}
}
